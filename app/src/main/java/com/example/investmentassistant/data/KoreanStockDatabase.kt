    fun search(query: String): List<FinnhubSymbolResult> =
        stocks
            .filter { stock ->
                stock.name.contains(query, ignoreCase = true) ||
                stock.symbol.contains(query, ignoreCase = true)
            }
            .take(15)
            .map { stock ->
                FinnhubSymbolResult(
                    symbol = stock.symbol,
                    displaySymbol = stock.symbol,
                    description = stock.name,
                    type = stock.type,
                )
            }
